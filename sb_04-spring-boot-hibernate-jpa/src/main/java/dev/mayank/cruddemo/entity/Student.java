package dev.mayank.cruddemo.entity;


import jakarta.persistence.*;

/**
 * @Entity: This is the java class mapped to the DB table
 */
@Entity
@Table(name = "student")
public class Student {

    @Id //PrimaryKey
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Column(name = "id")
    private int id;

    @Column(name = "first_name")
    private String firstName;

    @Column(name = "last_name")
    private String lastName;

    @Column(name = "email")
    private String email;

    // This constructor is a mandatory due to @Entity
    public Student() {
    }

    public Student(String firstName, String lastName, String email) {
        this.firstName = firstName;
        this.lastName = lastName;
        this.email = email;
    }

    public int getId() {
        return id;
    }

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    @Override
    public String toString() {
        return "Student[id=%d, firstName='%s', lastName='%s', email='%s']".formatted(id, firstName, lastName, email);
    }
}